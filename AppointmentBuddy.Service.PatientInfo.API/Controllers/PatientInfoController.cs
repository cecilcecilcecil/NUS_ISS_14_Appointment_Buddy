using AppointmentBuddy.Service.PatientInfo.API.Models;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Threading.Tasks;

namespace AppointmentBuddy.Service.PatientInfo.API.Controllers
{
    public class PatientInfoController : Controller
    {
        private readonly ILogger<PatientInfoController> _logger;

        public PatientInfoController(ILogger<PatientInfoController> logger)
        {
            _logger = logger;
        }

        public IActionResult Index()
        {
            return View();
        }

        public IActionResult Privacy()
        {
